const COMPANY_SHEET_NAME = "Company_tracking";
const APPLICATIONS_SHEET_NAME = "Applications";
const RSA_SHEET_NAME = "RSA_Reports";

export const RSA_REPORT_HEADERS = [
  "RSA ID",
  "Company ID",
  "Company Name",
  "Generated Date",
  "Generated By",
  "Candidate Sharing Strategy",
  "Interview Focus",
  "Company Expectations",
  "Training Gap",
  "Additional Admin Notes",
  "Tags",
  "Total Applications",
  "Selected Count",
  "Rejected Count",
  "In Progress Count",
  "Dropped Count",
  "AI Generated RSA",
  "Final Edited RSA",
  "Status",
  "Confirmed At",
  "Updated At",
] as const;

export async function getRsaOverview(companyId: string) {
  const company = await getCompanyFromCompanySheet(companyId);

  if (!company) {
    return null;
  }

  const applications = await getApplicationsForCompany(companyId);
  const applicationsSummary = summarizeApplications(applications);
  const stats = calculateStats(applicationsSummary);
  const history = await getRsaReportsForCompany(companyId);

  return {
    company,
    stats,
    existingRsa: history[0] ?? null,
    history,
    applicationsSummary,
  };
}

export async function getLatestRsaForCompany(companyId: string) {
  const history = await getRsaReportsForCompany(companyId);
  return history[0] ?? null;
}

export async function getAllRsaReports() {
  const sheet = await readRsaSheet();

  return sheet.rows
    .map(rowToRsaReport)
    .filter((report): report is RsaReportRecord => Boolean(report))
    .sort(sortReportsNewestFirst);
}

export async function getRsaReportsForCompany(companyId: string) {
  const reports = await getAllRsaReports();

  return reports
    .filter((report) => report.companyId === companyId)
    .sort(sortReportsNewestFirst);
}

export async function generateAndSaveCompanyRsa(input: {
  companyId: string;
  adminInputs: RsaAdminInputs;
}) {
  const overview = await getRsaOverview(input.companyId);

  if (!overview) {
    throw new Error("Company not found.");
  }

  const generatedRsa = await generateCompanyRsaReport({
    company: overview.company,
    stats: overview.stats,
    applicationsSummary: overview.applicationsSummary,
    adminInputs: input.adminInputs,
  });

  const report = await saveRsaReport({
    company: overview.company,
    adminInputs: input.adminInputs,
    stats: overview.stats,
    aiGeneratedRsa: generatedRsa,
    finalEditedRsa: generatedRsa,
    status: "Generated",
  });

  return {
    company: overview.company,
    stats: overview.stats,
    applicationsSummary: overview.applicationsSummary,
    generatedRsa,
    report,
  };
}

